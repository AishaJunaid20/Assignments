#Lesson:06 Lists and Tuples

# List Example - A list of fruits
fruits = ["Apple", "Banana", "Cherry"]
print("List of Fruits:", fruits)

# Accessing elements in a list
print("First Fruit:", fruits[0])

# Adding an item to the list
fruits.append("Orange")
print("Updated List:", fruits)

# Tuple Example - Immutable collection of colors
colors = ("Red", "Green", "Blue")
print("Tuple of Colors:", colors)

# Accessing elements in a tuple
print("Second Color:", colors[1])

# Dictionary Example - Storing information about a student
student = {
    "name": "Aisha",
    "age": 17,
    "course": "Cloud Computing"
}
print("Student Information:", student)

# Accessing values in a dictionary
print("Student Name:", student["name"])

# Updating dictionary values
student["age"] = 18
print("Updated Student Age:", student["age"])
